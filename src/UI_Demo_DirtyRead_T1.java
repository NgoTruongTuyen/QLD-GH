    private void loadDataToTable2() {
        int nums;
        try{
            conn = DBInfo.connect();
            pstmt = conn.prepareStatement("select * from TAIKHOAN");

            rs = pstmt.executeQuery();
            ResultSetMetaData stData = rs.getMetaData();

            nums = stData.getColumnCount();

            DefaultTableModel recordTable2 = (DefaultTableModel) tableAccount2.getModel();
            recordTable2.setRowCount(0);
            
            while (rs.next()) {
                boolean isDriver = false;
                Vector columnData = new Vector();
                String typeString;
                for (int i = 1; i <= nums; i++) {
                    int type = rs.getInt("LOAIND");
                    if (type == 3) {
                        isDriver = true;
                        
                        columnData.add(rs.getString("MATK"));
                        columnData.add(rs.getString("MATKHAU"));
                        columnData.add("Tài xế");
                        int status = rs.getInt("TINHTRANG");
                        columnData.add(status);
                        String statusString;
                        if (status == 0) {
                            statusString = new String ("Bị khóa");
                        }
                        else {
                            statusString = new String ("Bình thường");
                        }
                    }
                }
                if (isDriver) {
                    recordTable2.addRow(columnData);
                }
                
            }

        } catch(Exception ex){
            JOptionPane.showMessageDialog(null, "Không thể xem thông tin");
        }
    }
    
