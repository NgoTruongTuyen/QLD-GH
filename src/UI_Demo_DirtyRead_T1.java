    private void btnRunErrrorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRunErrrorActionPerformed
        DefaultTableModel recordTable = (DefaultTableModel)tableAccount1.getModel();
        int selectedRows = tableAccount1.getSelectedRow();
        String userID = recordTable.getValueAt(selectedRows, 0).toString();
        userID = userID.replaceAll("\\s+", "");
        String pwd = recordTable.getValueAt(selectedRows, 1).toString();
        pwd = pwd.replaceAll("\\s+", "");
        
        
        try {
            conn = DBInfo.connect();
            pstmt = conn.prepareStatement("EXEC SP_CAPNHATTK ?, ?, ?");
            pstmt.setString(1, txtAccountID.getText());
            String password = new String(txtPassword.getPassword());
            if (password.length() != 0) {
                pstmt.setString(2, password);
            } else {
                pstmt.setString(2, pwd);
            }
            pstmt.setString(3, txtStatus.getText());
            pstmt.executeUpdate();

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Có lỗi xảy ra, cập nhật không thành công!");
        }

        this.loadDataToTable2();
    }//GEN-LAST:event_btnRunErrrorActionPerformed

