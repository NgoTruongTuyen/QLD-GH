    public void loadData() {
        txtDriverID.setText(userID);
        int nums;
        
        try {
            conn = DBInfo.connect();
            pstmt = conn.prepareStatement("select * from TAIXE where MATX = ?");
            pstmt.setString(1, userID);
            rs = pstmt.executeQuery();
            String area = "";
            while (rs.next()) {
                txtPhone.setText(rs.getString("SDT"));
                area = new String(rs.getString("KHUVUCHD"));
                txtArea.setText(area);
            }

            //load data from QUANTRIVIEN table
            pstmt = conn.prepareStatement("select * from DONHANG where DIACHIGIAO = ?");
            pstmt.setString(1, area);
            rs = pstmt.executeQuery();
            
            rs = pstmt.executeQuery();
            ResultSetMetaData stData = rs.getMetaData();
            
            //nums = stData.getColumnCount();
            nums = stData.getColumnCount();
            DefaultTableModel recordTable = (DefaultTableModel) tableDonHang.getModel();
            recordTable.setRowCount(0);
            while (rs.next()) {
                Vector columnData = new Vector();
                for (int i = 0; i < nums; i++) {
                    columnData.add(rs.getString("MADH"));
                    columnData.add(rs.getString("MADT"));
                    columnData.add(rs.getString("MAKH"));
                    columnData.add(rs.getString("MATX"));
                    columnData.add(rs.getString("TENDH"));
                    int type = Integer.parseInt(rs.getString("TINHTRANGDH"));
                    if (type == 0) {
                        columnData.add("CHỜ TÀI XẾ");
                    }
                    else {
                        columnData.add("TÀI XẾ ĐÃ LẤY HÀNG");
                    }
                    
                    columnData.add(Integer.parseInt(rs.getString("PHIVANCHUYEN")));
                    columnData.add(rs.getString("DIACHIGIAO"));
                    columnData.add(rs.getString("HINHTHUCTT")); 
                }
                
                recordTable.addRow(columnData);
            }

        } catch (SQLException ex) {
            Logger.getLogger(ThongTinQuanTriVien.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
