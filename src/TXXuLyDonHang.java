    private void txtGetPackageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtGetPackageActionPerformed
        DefaultTableModel recordTable = (DefaultTableModel)tableDonHang.getModel();
        int selectedRows = tableDonHang.getSelectedRow();
        String packageID = recordTable.getValueAt(selectedRows, 0).toString();
        packageID = packageID.replaceAll("\\s+", "");
        String driverID = recordTable.getValueAt(selectedRows, 3).toString();
        driverID = driverID.replaceAll("\\s+", "");
        if (driverID.equals("TX0000")) {
            try {
                conn = DBInfo.connect();
                pstmt = conn.prepareStatement("update DONHANG SET MATX = ?, TINHTRANGDH = 1 where MADH = ?");
                pstmt.setString(1, userID);
                pstmt.setString(2, packageID);
                pstmt.executeUpdate();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Có lỗi xảy ra, cập nhật không thành công!");
            }
            this.loadData();
            JOptionPane.showMessageDialog(null, "Tiếp nhận đơn hàng thành công!");  
        }
        else {
           JOptionPane.showMessageDialog(null, "Đơn hàng này đã có tài xế giao, vui lòng chọn đơn hàng khác!"); 
        }
    }//GEN-LAST:event_txtGetPackageActionPerformed

